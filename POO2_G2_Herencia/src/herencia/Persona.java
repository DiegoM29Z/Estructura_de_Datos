
package herencia;

// esta clase lo que hace va a hacer es heredar los datos de nombre ,apellido y edad hacia su clase hija estudiante
public class Persona {
    private  String nombre,apellidos;
    private int edad;
    

    public Persona(String nombre, String apellidos, int edad) {
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.edad = edad;
       
    }

    public String getNombre() {
        return nombre;
    }

    public String getApellidos() {
        return apellidos;
    }

    public int getEdad() {
        return edad;
    }

    

    }

 

  
    


 
    

   
    


    
    

